    fn enqueue_clip<'a>(
        lifecycle: &mut TestLifecycle<'a>,
        counters: &mut AudioCounters,
        payload: &'a [u8],
        samples: u32,
        volume: MixerVolume,
        loop_metadata: ClipLoop,
    ) -> crate::SourceId {
        let mut clip = ClipAsset::pcm16_mono(policy().limits.sample_rate_hz, samples, payload);
        clip.loop_metadata = loop_metadata;
        lifecycle
            .enqueue(
                SourceRequest {
                    clip: Some(clip),
                    volume,
                    ..SourceRequest::default()
                },
                counters,
                DropPolicy::RejectNew,
            )
            .unwrap()
    }

    fn promote_all(lifecycle: &mut TestLifecycle<'_>, counters: &mut AudioCounters) {
        while lifecycle.promote_next(counters).is_some() {}
    }

