    mod heapless_payload {
        #[derive(Clone, Copy)]
        pub struct Payload {
            bytes: [u8; 16],
            len: usize,
        }

        impl Payload {
            pub fn from_samples(samples: &[i16]) -> Self {
                let mut bytes = [0; 16];
                let mut len = 0;
                for sample in samples {
                    let encoded = sample.to_le_bytes();
                    bytes[len] = encoded[0];
                    bytes[len + 1] = encoded[1];
                    len += 2;
                }

                Self { bytes, len }
            }

            pub fn as_slice(&self) -> &[u8] {
                &self.bytes[..self.len]
            }
        }
    }
